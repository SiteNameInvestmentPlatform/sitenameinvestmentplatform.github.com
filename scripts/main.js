

//Mobile menu dropdown toggle
document.addEventListener('DOMContentLoaded', function () {

    // Get all "navbar-burger" elements
    var $navbarBurgers = Array.prototype.slice.call(document.querySelectorAll('.navbar-burger'), 0);

    // Check if there are any navbar burgers
    if ($navbarBurgers.length > 0) {

        // Add a click event on each of them
        $navbarBurgers.forEach(function ($el) {
            $el.addEventListener('click', function () {

                // Get the target from the "data-target" attribute
                var target = $el.dataset.target;
                var $target = document.getElementById(target);

                // Toggle the class on both the "navbar-burger" and the "navbar-menu"
                $el.classList.toggle('is-active');
                $target.classList.toggle('is-active');

            });
        });
    }

});


//Control Modal
function toggleModal() {
    var modal = document.getElementById("modal");
    if (modal.className == "modal") {
        modal.className = "modal is-active";
    } else {
        modal.className = "modal";
    }
}

/* Toggle investments (Active or History) */
function toggleInvestments(clicked) {
    var active = document.getElementById("investmentsActive");
    var history = document.getElementById("investmentsHistory");
    if (clicked == "active") {
        active.className = "button is-large is-dark";
        history.className = "button is-large is-dark is-outlined";
    }
    if (clicked == "history") {
        history.className = "button is-large is-dark";
        active.className = "button is-large is-dark is-outlined";
    }
}


